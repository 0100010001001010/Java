/*
 * (Print a table) Write a program that displays the following table:
 *  a  a^2  a^3
 *  1   1    1
 *  2   4    8
 *  3   9    27
 *  4   16   64
 */

public class PrintATable {

    public static void main(String[] args) {
        System.out.println("  _________________________");
        System.out.println("|__a__|___a^2___|___a^3___|");
        System.out.println("|__1__|____1____|____1____|");
        System.out.println("|__2__|____4____|____8____|");
        System.out.println("|__3__|____9____|___27____|");
        System.out.println("|__4__|___16____|___64____|");
    }
}
